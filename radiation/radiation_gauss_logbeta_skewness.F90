contains
  !---------------------------------------------------------------------
  ! Compute the weights and nodes for integrating across a logbeta
  ! distribution given the fractional standard deviation (=standard
  ! deviation divided by the mean) and the skewness (of the underlying
  ! beta distribution) assuming a mean of unity, using Gauss-Jacobi
  ! Quadrature. The user can then multiply the nodes output from this
  ! routine by the mean of the distribution.  If the quadrature order
  ! "norder" is less than 1, the weights and nodes will not be
  ! written, while if it is larger than 7, only 7 nodes will be
  ! written and the remainder will be assigned zero weight.
  subroutine calc_gauss_logbeta_skewness(ng, norder, fsd, skewness, weights, nodes)

    ! Number of independent sets of weights required (typically this
    ! corresponds to spectral interval or atmospheric column)
    integer(jpim), intent(in)  :: ng
    ! Number of nodes to be used for the quadrature
    integer(jpim), intent(in)  :: norder
    ! Fractional standard deviation
    real(jprb),    intent(in)  :: fsd(ng)
    ! Skewness
    real(jprb),    intent(in)  :: skewness
    
    ! Weighting of each node
    real(jprb),    intent(out) :: weights(ng,norder)
    ! Value of each node assuming a mean of one
    real(jprb),    intent(out) :: nodes(ng,norder)

    ! Index for look-up table in fractional standard deviation
    integer(jpim) :: ifsd(ng)
    ! Weights for look-up table
    real(jprb)    :: w1fsd(ng), w2fsd(ng)

    ! Index for look-up table in skewness
    integer(jpim) :: iskew
    ! Weights for look-up table
    real(jprb)    :: w1skew, w2skew

    ! Local scale factor and add offset
    real(jprb)    :: scale_fact, add_offs
    
    ! Capped quadrature order
    integer(jpim) :: myorder

    ! Loop index
    integer(jpim) :: jg

    ! Compute FSD look-up table indices as real numbers
    w1fsd = 1.0_jprb + max(0.0_jprb, min(fsd / dfsd, nfsd-1.00001_jprb))
    ! Indices of the first point in the interpolation
    ifsd = floor(w1fsd)
    ! Weight of the second point in the interpolation
    w2fsd = w1fsd - ifsd
    ! Weight of the first point in the interpolation
    w1fsd = 1.0_jprb - w2fsd

    ! Compute skewness look-up table indices as real numbers
    w1skew = 1.0_jprb + max(0.0_jprb, min((skewness-skew0) / dskew, nskew-1.00001_jprb))
    ! Indices of the first point in the interpolation
    iskew = floor(w1skew)
    ! Weight of the second point in the interpolation
    w2skew = w1skew - iskew
    ! Weight of the first point in the interpolation
    w1skew = 1.0_jprb - w2skew

    myorder = min(nmaxorder, norder)

    if (myorder > 1) then
      do jg = 1,ng
        weights(jg,1:myorder) = w1skew * weights_lut(1:myorder,myorder,iskew) &
             &                + w2skew * weights_lut(1:myorder,myorder,iskew+1)
        nodes(jg,1:myorder)   = w1skew * nodes_lut(1:myorder,myorder,iskew) &
             &                + w2skew * nodes_lut(1:myorder,myorder,iskew+1)
        scale_fact = w1skew * (w1fsd(jg)*scale_factor(ifsd(jg),iskew) + w2fsd(jg)*scale_factor(ifsd(jg)+1,iskew)) &
             &     + w2skew * (w1fsd(jg)*scale_factor(ifsd(jg),iskew+1)+w2fsd(jg)*scale_factor(ifsd(jg)+1,iskew+1))
        add_offs   = w1skew * (w1fsd(jg)*  add_offset(ifsd(jg),iskew) + w2fsd(jg)*  add_offset(ifsd(jg)+1,iskew)) &
             &     + w2skew * (w1fsd(jg)*  add_offset(ifsd(jg),iskew+1)+w2fsd(jg)*  add_offset(ifsd(jg)+1,iskew+1))
        nodes(jg,1:myorder)   = exp(nodes(jg,1:myorder)*scale_fact + add_offs)
        !nodes(jg,1:myorder) = nodes(jg,1:myorder) &
        !     &                / sum(weights(jg,1:myorder)*nodes(jg,1:myorder))
      end do
      weights(:,myorder+1:) = 0.0_jprb
      nodes(:,myorder+1:)   = 1.0_jprb
    else
      weights(:,1)  = 1.0_jprb
      weights(:,2:) = 0.0_jprb
      nodes         = 1.0_jprb
    end if

  end subroutine calc_gauss_logbeta_skewness

end module radiation_gauss_logbeta_skewness
